n1,n2=input().split(",")
num1=int(n1)
num2=int(n2)
print(f"{num1}+{num2}={num1+num2}")
print(f"{num1}-{num2}={num1-num2}")
print(f"{num1}*{num2}={num1*num2}")
print(f"{num1}/{num2}={num1/num2}")
print(f"{num1}//{num2}={num1//num2}")
print(f"{num1}%{num2}={num1%num2}")
print(f"{num1}**{num2}={num1**num2}")