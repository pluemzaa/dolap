user_input=input().split(",")

print("{}+{}={}".format(user_input[0], user_input[1], int(user_input[0])+int(user_input[1])))
print("{}-{}={}".format(user_input[0], user_input[1], int(user_input[0])-int(user_input[1])))
print("{}*{}={}".format(user_input[0], user_input[1], int(user_input[0])*int(user_input[1])))
print("{}/{}={}".format(user_input[0], user_input[1], int(user_input[0])/int(user_input[1])))
print("{}//{}={}".format(user_input[0], user_input[1], int(user_input[0])//int(user_input[1])))
print("{}%{}={}".format(user_input[0], user_input[1], int(user_input[0])%int(user_input[1])))
print("{}**{}={}".format(user_input[0], user_input[1], int(user_input[0])**int(user_input[1])))