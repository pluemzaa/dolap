A = input()
B = A.split(',')
print(B[0],'+',B[1],'=', int(B[0]) + int(B[1]))
print(B[0],'-',B[1],'=', int(B[0]) - int(B[1]))
print(B[0],'*',B[1],'=', int(B[0]) * int(B[1]))
print(B[0],'/',B[1],'=', int(B[0]) / int(B[1]))
print(B[0],'//',B[1],'=', int(B[0]) // int(B[1]))
print(B[0],'%',B[1],'=', int(B[0]) % int(B[1]))
print(B[0],'**',B[1],'=', int(B[0]) ** int(B[1]))