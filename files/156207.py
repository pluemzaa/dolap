A = input()
X = A.split(',')
S = int(X[0])+int(X[1])
M = int(X[0])-int(X[1])
ML = int(X[0])*int(X[1])
D = int(X[0])/int(X[1])
DD = int(X[0])//int(X[1])
P = int(X[0])%int(X[1])
G = int(X[0])**int(X[1])
print(X[0] ,"+" ,X[1] ,'=' , S)
print(X[0] ,"-" ,X[1] ,'=' , M)
print(X[0] ,"*" ,X[1] ,'=' , ML)
print(X[0] ,"/" ,X[1] ,'=' , D)
print(X[0] ,"//" ,X[1] ,'=' , DD)
print(X[0] ,"%" ,X[1] ,'=' , P)
print(X[0] ,"**" ,X[1] ,'=' , G)