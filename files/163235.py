# บรรทัดสามรับจำนวนลูกชิ้นที่มีใน 1 ถุง
# # ผลลัพธ์เป็นจำนวนเต็ม

stick = int(input())
balls = int(input())
back_balls = int(input())

if(((stick * balls)%back_balls) == 0):
    print(int((stick * balls)/back_balls))
else:
    print(int(((stick * balls)/back_balls)+1))