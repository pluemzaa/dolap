# รับเวกเตอร์ตัวแรกจากผู้ใช้
v1 = input("ป้อนเวกเตอร์ที่ 1 (คั่นด้วยช่องว่าง เช่น 1 2 3): ")
# แปลงเป็นลิสต์ของจำนวนเต็ม
v1 = list(map(int, v1.split()))

# รับเวกเตอร์ตัวที่สองจากผู้ใช้
v2 = input("ป้อนเวกเตอร์ที่ 2 (คั่นด้วยช่องว่าง เช่น 4 5 6): ")
# แปลงเป็นลิสต์ของจำนวนเต็ม
v2 = list(map(int, v2.split()))

# ตรวจสอบว่าแต่ละเวกเตอร์มี 3 ตัว
if len(v1) != 3 or len(v2) != 3:
    print("เวกเตอร์แต่ละตัวต้องมี 3 สมาชิกเท่านั้น")
else:
    # คำนวณ Dot Product
    dot_product = sum(v1[i] * v2[i] for i in range(3))
    print("Dot Product =", dot_product)