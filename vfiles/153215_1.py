x = input("").split(',')
a = int(x[0])
b = int(x[1])

# การบวก
print(f"{a}+{b}={a+b}")

# การลบ
print(f"{a}-{b}={a-b}")

# การคูณ
print(f"{a}*{b}={a*b}")

# การหาร (ทศนิยม)
print(f"{a}/{b}={a/b}")

# การหาร (ปัดเศษลง)
print(f"{a}//{b}={a//b}")

# การหาร (เศษ)
print(f"{a}%{b}={a%b}")

# การยกกำลัง
print(f"{a}**{b}={a**b}")