data=input()
data=data.split(",")
num1=int(data[0])
num2=int(data[1])
print(f"{num1}+{num2}={num1+num2}")
print(f"{num1}-{num2}={num1-num2}")
print(f"{num1}*{num2}={num1*num2}")
print(f"{num1}/{num2}={num1/num2}")
print(f"{num1}//{num2}={num1//num2}")
print(f"{num1}%{num2}={num1%num2}")
print(f"{num1}**{num2}={num1**num2}")