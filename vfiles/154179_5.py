number = input()
split_number = number.split(',')
num_1 = float(split_number[0])
num_2 = float(split_number[1])
print(str(num_1) + "+" + str(num_2) + "=" + str(num_1 + num_2))
print(str(num_1) + "-" + str(num_2) + "=" + str(num_1 - num_2))
print(str(num_1) + "*" + str(num_2) + "=" + str(num_1 * num_2))
print(str(num_1) + "/" + str(num_2) + "=" + str(num_1 / num_2))
print(str(num_1) + "//" + str(num_2) + "=" + str(num_1 // num_2))
print(str(num_1) + "%" + str(num_2) + "=" + str(num_1 % num_2))
print(str(num_1) + "**" + str(num_2) + "=" + str(num_1 ** num_2))