# ทดสอบการเปรียบเทียบตัวแปรด้วย is (เปรียบเทียบตำแหน่งในหน่วยความจำ)

x = list("Python")
y = list("Python")

# แสดงค่าของทั้งสองตัวแปร
print("x =", x)
print("y =", y)

# เปรียบเทียบว่าเป็น object เดียวกันหรือไม่
print("x is y:", x is y)

# แสดงตำแหน่งหน่วยความจำของตัวแปรแต่ละตัว
print("id(x):", id(x))
print("id(y):", id(y))