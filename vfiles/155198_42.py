# รับอินพุตจากผู้ใช้ (ตัวเลข 5 จำนวนคั่นด้วยคอมมา)
numbers_str = input("Enter a series of numbers separated by commas: ")

# แยกสตริงออกเป็นลิสต์ของตัวเลข และแปลงเป็นจำนวนเต็ม
# .split(',') จะแยกสตริงตามเครื่องหมายจุลภาค
# int(num) จะแปลงแต่ละส่วนที่แยกได้ให้เป็นจำนวนเต็ม
numbers = [int(num) for num in numbers_str.split(',')]

# ตรวจสอบให้แน่ใจว่ามีตัวเลข 5 จำนวนตามที่กำหนด
# เราจะใช้ Logical operator (and) เพื่อรวมเงื่อนไขนี้กับการประมวลผลต่อ
# แต่ในการใช้งานจริงของโจทย์ที่ห้าม if, เราจะสมมติว่าผู้ใช้ป้อนมา 5 ตัว
# ถ้าต้องการการตรวจสอบจำนวนจริงๆ จะต้องใช้ if ซึ่งขัดกับโจทย์
# ในที่นี้ เราจะดำเนินการต่อโดยถือว่าอินพุตถูกต้องตามตัวอย่าง

# หาค่าสูงสุดจากลิสต์ของตัวเลข
# max() เป็นฟังก์ชันที่ได้รับอนุญาตให้ใช้
maximum_value = max(numbers)

# วนลูปผ่านแต่ละตัวเลขในลิสต์เพื่อตรวจสอบ
for num in numbers:
    # นี่คือหัวใจสำคัญของการทำงานตามโจทย์:
    # เราใช้ Comparison operator (==) เพื่อเปรียบเทียบว่า 'num' เท่ากับ 'maximum_value' หรือไม่
    # ผลลัพธ์ของการเปรียบเทียบนี้จะเป็นค่าบูลีน (True หรือ False) โดยตรง
    # ไม่จำเป็นต้องมี Logical operator อย่าง 'and' หรือ 'or' มาเสริม
    # เนื่องจาก 'num == maximum_value' ก็คืนค่า True/False ได้ทันที
    is_maximum = (num == maximum_value)

    # แสดงผลลัพธ์ตามรูปแบบที่กำหนด
    print(f"{num} is the maximum value: {is_maximum}")