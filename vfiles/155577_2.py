input_str = input()
num1_str,num2_str = input_str.split (",")
num1 = int(num1_str)
num2 = int(num2_str)
print(f"{num1}+{num2}={num1+num2}")
print(f"{num1}-{num2}={num1-num2}")
print(f"{num1}*{num2}={num1*num2}")
print(f"{num1}/{num2}={num1/num2}")
print(f"{num1}//{num2}={num1//num2}")
print(f"{num1}%{num2}={num1%num2}")
print(f"{num1}**{num2}={num1**num2}")