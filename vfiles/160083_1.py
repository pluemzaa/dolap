# โปรแกรมคำนวณภาษีเงินได้บุคคลธรรมดา (เวอร์ชันนักเรียน)

# ขั้นตอนที่ 1: รับเงินได้สุทธิจากผู้ใช้งาน
print("โปรดป้อนเงินได้สุทธิของคุณ (จำนวนเงินตัวเลขเท่านั้น):")
net_income_str = input() # รับค่าเป็นข้อความก่อน

# ขั้นตอนที่ 2: แปลงข้อความเป็นตัวเลข
net_income = float(net_income_str) # แปลงข้อความเป็นตัวเลขทศนิยม

# ขั้นตอนที่ 3: กำหนดตัวแปรสำหรับภาษี
tax_amount = 0.0 # กำหนดค่าเริ่มต้นเป็นศูนย์

# ขั้นตอนที่ 4: คำนวณภาษีตามเงื่อนไข (ขั้นบันได)

# ขั้นที่ 1: เงินได้สุทธิ 0 - 150,000 บาท (ยกเว้นภาษี)
if net_income >= 0 and net_income <= 150000:
    tax_amount = 0

# ขั้นที่ 2: เงินได้สุทธิ 150,001 - 300,000 บาท (อัตรา 5%)
elif net_income > 150000 and net_income <= 300000:
    tax_amount = (net_income - 150000) * 0.05

# ขั้นที่ 3: เงินได้สุทธิ 300,001 - 500,000 บาท (อัตรา 10%)
elif net_income > 300000 and net_income <= 500000:
    tax_amount = ((net_income - 300000) * 0.10) + 7500

# ขั้นที่ 4: เงินได้สุทธิ 500,001 - 750,000 บาท (อัตรา 15%)
elif net_income > 500000 and net_income <= 750000:
    tax_amount = ((net_income - 500000) * 0.15) + 27500

# ขั้นที่ 5: เงินได้สุทธิ 750,001 - 1,000,000 บาท (อัตรา 20%)
elif net_income > 750000 and net_income <= 1000000:
    tax_amount = ((net_income - 750000) * 0.20) + 65000

# ขั้นที่ 6: เงินได้สุทธิ 1,000,001 - 2,000,000 บาท (อัตรา 25%)
elif net_income > 1000000 and net_income <= 2000000:
    tax_amount = ((net_income - 1000000) * 0.25) + 115000

# ขั้นที่ 7: เงินได้สุทธิ 2,000,001 - 5,000,000 บาท (อัตรา 30%)
elif net_income > 2000000 and net_income <= 5000000:
    tax_amount = ((net_income - 2000000) * 0.30) + 365000

# ขั้นที่ 8: เงินได้สุทธิมากกว่า 5,000,000 บาท (อัตรา 35%)
elif net_income > 5000000:
    tax_amount = ((net_income - 5000000) * 0.35) + 1265000

# ถ้ามีกรณีที่เงินได้สุทธิเป็นลบ (ไม่น่าจะเกิดขึ้นจริง แต่เผื่อไว้)
else:
    print("เงินได้สุทธิไม่สามารถเป็นค่าติดลบได้ กรุณาป้อนค่าที่ถูกต้อง")
    tax_amount = 0 # กำหนดเป็น 0 หรือค่าอื่นๆ ตามต้องการ


# ขั้นตอนที่ 5: แสดงผลลัพธ์
print("--------------------------------------------------")
print("เงินได้สุทธิที่คุณป้อนคือ:", net_income)
print("จำนวนภาษีที่คุณต้องชำระในปีนี้คือ: ", tax_amount)
print("--------------------------------------------------")